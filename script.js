    const marqueeGroups = document.querySelectorAll('.marquee-group');
    let marqueeTween;

    function initMarquee() {
        if (marqueeGroups.length === 0) return;

        const isRTL = document.documentElement.getAttribute('dir') === 'rtl';
        // If RTL, move to Right (100%), if LTR move to Left (-100%)
        const progress = isRTL ? 100 : -100;

        // Kill previous if exists
        if (marqueeTween) marqueeTween.kill();

        // Reset positions
        gsap.set(marqueeGroups, { xPercent: 0 });

        marqueeTween = gsap.to(marqueeGroups, {
            xPercent: progress,
            repeat: -1,
            duration: 80,
            ease: "linear"
        }).totalProgress(0.5);
    }

